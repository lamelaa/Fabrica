// Inicializa la librería AOS (Animate On Scroll)
AOS.init({
    duration: 800, // Duración de la animación en milisegundos (800ms)
    once: true, // La animación se ejecutará solo una vez, no se repetirá
    easing: 'ease-in-out', // Efecto de aceleración de la animación, pasando de lento a rápido y luego a lento
});
